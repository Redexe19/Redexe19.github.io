function projectPageUrl(id){
  const prefix = location.pathname.includes('/minecraft/') || location.pathname.includes('/roblox/')
    ? '../'
    : '';
  return `${prefix}project.html?id=${encodeURIComponent(id)}`;
}

function projectCard(id, tag){
  const project = projectDetails[id];
  if(!project) return null;
  return {
    title: project.title,
    tag: tag || project.tag,
    description: project.summary,
    meta: [project.category, project.status, project.availability],
    features: project.features.slice(0, 3),
    page: projectPageUrl(id),
    primary: tag === 'Featured',
    icon: project.icon
  };
}

function compactProjects(ids, tag){
  return ids.map(id => projectCard(id, tag)).filter(Boolean);
}
